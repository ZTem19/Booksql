    class DeleteButtonEditor extends DefaultCellEditor 
    {
        private JButton button;
        private boolean clicked;
        private int selectedRow;

        public DeleteButtonEditor(JCheckBox checkBox) {
            super(checkBox);
            button = new JButton("Delete");
            button.setOpaque(true);
            button.setForeground(Color.WHITE);
            button.setBackground(Color.RED);

            button.addActionListener(new ActionListener() {
                public void actionPerformed(ActionEvent e) {
                    fireEditingStopped(); // Stops editing
                }
            });
        }

        public Component getTableCellEditorComponent(JTable table, Object value,
        boolean isSelected, int row, int column) {
            clicked = true;
            selectedRow = row;
            return button;
        }

        public Object getCellEditorValue() {
            if (clicked) {
                
                int authorId = (int) author_table.getValueAt(selectedRow, 0);
                int response = JOptionPane.showConfirmDialog(button,
                    "Are you sure you want to delete author id " + authorId + "?",
                    "Confirm Delete", JOptionPane.YES_NO_OPTION);

                if (response == JOptionPane.YES_OPTION) {
                    try {
                        BookDAO bookDAO = DatabaseAccess.getBookDAO();
                        if(bookDAO.getAuthorCountForBook(id) <= 1)
                        {
                            JOptionPane.showMessageDialog(button, "The book must have at least one author.");
                            clicked = false;
                            return "Delete";
                        }
                        bookDAO.deleteAuthorFromBook(authorId, id);
                        JOptionPane.showMessageDialog(button, "Author deleted from book.");
                        loadAuthors();
                    } catch (SQLException e) {
                        JOptionPane.showMessageDialog(button, "Error deleting author: " + e.getMessage());
                        e.printStackTrace();
                    }
                }
                
                selectedRow = 0;
            }
            clicked = false;
            return "Delete";
        }

        public boolean stopCellEditing() {
            clicked = false;
            return super.stopCellEditing();
        }

        protected void fireEditingStopped() {
            try
            {
                super.fireEditingStopped();
            }
            catch (Exception e)
            {
            }
        }
    }
