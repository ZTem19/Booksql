    public void loadBook()
    {
        try 
        {
            BookDAO bookDAO = DatabaseAccess.getBookDAO();
            book = bookDAO.getBookById(id);
        } 
        catch (SQLException e) 
        {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Error loading books: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }
    }
    
    public void loadAuthors()
    {
        try
        {
            BookDAO bookDAO = DatabaseAccess.getBookDAO();
            ArrayList<Author> authors = bookDAO.getAuthorsByBookId(id);
            
            authorTableModel.setRowCount(0);
            for (Author a : authors)
            {
                authorTableModel.addRow(new Object[]{
                    a.getAuthorId(),
                    a.getFirstName(),
                    a.getLastName(),
                    a.getBirthDate()
                });
            }
        }
        catch (SQLException e) 
        {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Error loading authors: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }
    }
    
